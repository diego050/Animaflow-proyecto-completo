export type PropType =
  | 'string'
  | 'number'
  | 'color'
  | 'boolean'
  | 'select'
  | 'text-long'
  | 'icon'
  | 'list';

export interface PropDefinition {
  name: string;
  type: PropType;
  label: string;
  description: string;
  defaultValue?: string | number | boolean | (string | number)[] | Record<string, unknown>[];
  options?: string[];
  min?: number;
  max?: number;
}

export interface ComponentManifestEntry {
  name: string;
  category: string;
  role: string;
  description: string;
  props: PropDefinition[];
}

